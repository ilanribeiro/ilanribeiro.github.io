let a = 20
let b = 5

// Adição
let soma = a + b;
console.log(`O resultado da soma entre ${a} e ${b} é igual a: ${soma}`);

// Subtração
let subtracao = a - b;
console.log(`O resultado da subtração entre ${a} e ${b} é igual a: ${subtracao}`);

//Multiplicação
let multiplicacao = a * b;
console.log(`O resultado da multiplicação entre ${a} e ${b} é igual a: ${multiplicacao}`);

//Divisão
let divisao = a / b;
console.log(`O resultado da divisão entre ${a} e ${b} é igual a: ${divisao}`);

//Módulo
let modulo = a % b;
console.log(`O resultado do resto da divisão entre ${a} e ${b} é igual a: ${modulo}`);